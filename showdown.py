# Task 1: Identify the Greatest

 
print("Please enter 3 numbers:")
first = int(input("1st number: "))
second = int(input("2nd number: "))
third = int(input("3rd number: "))

greatest = max(first,second,third)
smallest = min(first,second,third)

print(greatest, " is the greatest number.")
print(smallest, " is the smallest number.")

